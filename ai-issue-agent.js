#!/usr/bin/env node

/**
 * AI Issue Agent for Skonester Cheat Menu
 * 
 * Uses Gemini API to analyze GitHub issues and suggest fixes based on the codebase.
 */

const { Octokit } = require('@octokit/rest');
const fs = require('fs');
const path = require('path');

const REPO_OWNER = 'skonester';
const REPO_NAME = 'Skonester-Cheat-Menu';

class AIIssueAgent {
  constructor(githubToken, geminiApiKey) {
    this.octokit = new Octokit({ auth: githubToken });
    this.geminiApiKey = geminiApiKey;
  }

  async analyzeIssue(issueNumber) {
    console.log(`🤖 AI Agent: Analyzing issue #${issueNumber}...`);

    try {
      // 1. Get issue details
      const { data: issue } = await this.octokit.issues.get({
        owner: REPO_OWNER,
        repo: REPO_NAME,
        issue_number: issueNumber
      });

      const issueTitle = issue.title;
      const issueBody = issue.body || '';

      // 2. Find relevant files in the codebase
      const relevantFiles = await this.findRelevantFiles(issueTitle + ' ' + issueBody);
      console.log(`📄 Found ${relevantFiles.length} relevant files for context.`);

      // 3. Read file contents for context
      let context = '';
      for (const file of relevantFiles) {
        try {
          const content = fs.readFileSync(file, 'utf8');
          context += `\n--- File: ${file} ---\n${content.substring(0, 3000)}\n`; // Limit context size
        } catch (e) {
          console.warn(`Could not read file ${file}: ${e.message}`);
        }
      }

      // 4. Construct prompt for Gemini
      const prompt = `
You are an expert AI coding assistant for the "Skonester Cheat Menu" mod for Crusader Kings III.
A user has reported an issue on GitHub. Your task is to analyze the issue and suggest a fix based on the provided code context.

ISSUE TITLE: ${issueTitle}
ISSUE BODY:
${issueBody}

CODE CONTEXT:
${context}

Please provide:
1. **Potential Cause**: A brief explanation of why this might be happening.
2. **Suggested Fix**: Specific code changes or steps to resolve the issue.
3. **Confidence Level**: High, Medium, or Low.

Format your response in Markdown.
`;

      // 5. Call Gemini API
      const aiResponse = await this.callGemini(prompt);

      // 6. Post comment on GitHub
      await this.octokit.issues.createComment({
        owner: REPO_OWNER,
        repo: REPO_NAME,
        issue_number: issueNumber,
        body: `### 🤖 AI Agent Analysis\n\n${aiResponse}\n\n---\n*This analysis was automatically generated by the AI Issue Agent.*`
      });

      console.log(`✅ AI Agent: Successfully analyzed and commented on #${issueNumber}`);
    } catch (error) {
      console.error('❌ AI Agent Error:', error.message);
    }
  }

  async findRelevantFiles(searchText) {
    const relevantFiles = [];
    const keywords = searchText.toLowerCase().split(/\W+/);
    
    // Define important directories to scan
    const dirsToScan = ['events', 'common/decisions', 'common/on_action', 'gui'];
    
    for (const dir of dirsToScan) {
      const fullPath = path.join(process.cwd(), dir);
      if (fs.existsSync(fullPath)) {
        const files = this.getAllFiles(fullPath);
        for (const file of files) {
          const fileName = path.basename(file).toLowerCase();
          if (keywords.some(k => k.length > 3 && fileName.includes(k))) {
            relevantFiles.push(file);
          }
        }
      }
    }
    
    return relevantFiles.slice(0, 5); // Return top 5 matches
  }

  getAllFiles(dirPath, arrayOfFiles = []) {
    const files = fs.readdirSync(dirPath);

    files.forEach((file) => {
      if (fs.statSync(dirPath + "/" + file).isDirectory()) {
        arrayOfFiles = this.getAllFiles(dirPath + "/" + file, arrayOfFiles);
      } else {
        arrayOfFiles.push(path.join(dirPath, "/", file));
      }
    });

    return arrayOfFiles;
  }

  async callGemini(prompt) {
    const url = `https://generativelanguage.googleapis.com/v1beta/models/gemini-1.5-flash:generateContent?key=${this.geminiApiKey}`;
    
    const response = await fetch(url, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json'
      },
      body: JSON.stringify({
        contents: [{
          parts: [{ text: prompt }]
        }]
      })
    });

    if (!response.ok) {
      const error = await response.json();
      throw new Error(`Gemini API Error: ${JSON.stringify(error)}`);
    }

    const data = await response.json();
    return data.candidates[0].content.parts[0].text;
  }
}

// CLI Interface
async function main() {
  const githubToken = process.env.GITHUB_TOKEN;
  const geminiApiKey = process.env.GEMINI_API_KEY;
  const issueNumber = process.env.ISSUE_NUMBER;

  if (!githubToken || !geminiApiKey || !issueNumber) {
    console.error('Missing environment variables: GITHUB_TOKEN, GEMINI_API_KEY, or ISSUE_NUMBER');
    process.exit(1);
  }

  const agent = new AIIssueAgent(githubToken, geminiApiKey);
  await agent.analyzeIssue(parseInt(issueNumber));
}

main().catch(console.error);
