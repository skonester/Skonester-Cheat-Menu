#!/usr/bin/env node

// Optional: specify target base branch for PRs via environment variable TARGET_BASE_BRANCH

/**
 * AI Issue Agent for Skonester Cheat Menu
 * 
 * Uses Gemini API to analyze GitHub issues and automatically submit Pull Requests with fixes.
 */

const { Octokit } = require('@octokit/rest');
const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const REPO_OWNER = 'skonester';
const REPO_NAME = 'Skonester-Cheat-Menu';

class AIIssueAgent {
  constructor(githubToken, geminiApiKey) {
    this.octokit = new Octokit({ auth: githubToken });
    this.geminiApiKey = geminiApiKey;
  }

  async analyzeAndFix(issueNumber) {
    console.log(`🤖 AI Agent: Analyzing and attempting fix for issue #${issueNumber}...`);

    try {
      // 1. Get issue details
      const { data: issue } = await this.octokit.issues.get({
        owner: REPO_OWNER,
        repo: REPO_NAME,
        issue_number: issueNumber
      });

      const issueTitle = issue.title;
      const issueBody = issue.body || '';

      // 2. Find relevant files
      const relevantFiles = await this.findRelevantFiles(issueTitle + ' ' + issueBody);
      
      // 3. Construct context
      let context = '';
      for (const file of relevantFiles) {
        const content = fs.readFileSync(file, 'utf8');
        context += `\n--- File: ${file} ---\n${content.substring(0, 5000)}\n`;
      }

      // 4. Ask Gemini for a fix in JSON format
      const prompt = `
You are an expert CK3 modder. Fix the following issue.
ISSUE: ${issueTitle}
BODY: ${issueBody}

CONTEXT:
${context}

IMPORTANT: You must return a JSON object with exactly these fields:
{
  "explanation": "Why this happens",
  "file_to_fix": "relative/path/to/file",
  "find": "Exact string to find in the file",
  "replace": "Exact string to replace it with",
  "confidence": "High/Medium/Low"
}
Only return the JSON.
`;

      const rawAiResponse = await this.callGemini(prompt);
      const fix = JSON.parse(rawAiResponse.replace(/```json|```/g, ''));

      console.log(`💡 AI suggested fix for ${fix.file_to_fix} with ${fix.confidence} confidence.`);

      // 5. Apply fix and create PR if confidence is high/medium
      if (fix.confidence !== 'Low') {
        await this.submitPullRequest(issueNumber, fix);
      } else {
        await this.octokit.issues.createComment({
          owner: REPO_OWNER,
          repo: REPO_NAME,
          issue_number: issueNumber,
          body: `🤖 **AI Analysis**: ${fix.explanation}\n\nConfidence was too low to auto-fix. Please check manually.`
        });
      }

    } catch (error) {
      console.error('❌ Error:', error.message);
    }
  }

  async submitPullRequest(issueNumber, fix) {
    const branchName = `ai-fix-issue-${issueNumber}`;
    const filePath = path.join(process.cwd(), fix.file_to_fix);

    if (!fs.existsSync(filePath)) throw new Error(`File ${fix.file_to_fix} not found`);

    // Apply the change
    let content = fs.readFileSync(filePath, 'utf8');
    if (!content.includes(fix.find)) {
        console.warn("Could not find exact text to replace. Trying fuzzy match...");
        // Fallback or error
        throw new Error("Find text not found in target file.");
    }
    const newContent = content.replace(fix.find, fix.replace);
    fs.writeFileSync(filePath, newContent);

    // Git operations
    try {
        execSync(`git config user.name "AI Issue Agent"`);
        execSync(`git config user.email "ai-agent@skonester.com"`);
        execSync(`git checkout -b ${branchName}`);
        execSync(`git add ${fix.file_to_fix}`);
        
        // Check if there are actually changes to commit
        const status = execSync(`git status --porcelain`).toString();
        if (!status) {
            console.log("ℹ️ No changes detected. The fix might already be applied.");
            return;
        }

        execSync(`git commit -m "AI Fix for issue #${issueNumber}: ${fix.explanation.substring(0, 50)}"`);
        execSync(`git push origin ${branchName} --force`);

        // Create PR
        const targetBase = process.env.TARGET_BASE_BRANCH || 'main';
        const { data: pr } = await this.octokit.pulls.create({
          owner: REPO_OWNER,
          repo: REPO_NAME,
          title: `🤖 AI Fix for Issue #${issueNumber}`,
          head: branchName,
          base: targetBase,
          body: `This Pull Request was automatically generated by the AI Issue Agent to fix #${issueNumber}.\n\n### 📝 Analysis\n${fix.explanation}`
        });

        console.log(`🚀 Pull Request created: ${pr.html_url}`);
    } catch (gitError) {
        console.error(`❌ Git/PR Error: ${gitError.message}`);
    }
  }

  async findRelevantFiles(text) {
    // Scan the entire repository and return a list of source files to include in the AI context.
    // We ignore binary, dependency, and configuration directories that are not useful for fixing code.
    const repoRoot = process.cwd();
    const allFiles = [];
    const walk = (dir) => {
      const entries = fs.readdirSync(dir, { withFileTypes: true });
      for (const entry of entries) {
        const fullPath = path.join(dir, entry.name);
        // Skip common large or irrelevant directories
        if (entry.isDirectory()) {
          if ([".git", "node_modules", ".github", "dist", "build", "out"].includes(entry.name)) continue;
          walk(fullPath);
        } else {
          // Include typical source / text file extensions
          if (/\.(js|ts|json|txt|md|yml|yaml|css|html|scss|c|cpp|h|hpp|py|java|go|rb|sh)$/i.test(entry.name)) {
            allFiles.push(fullPath);
          }
        }
      }
    };
    walk(repoRoot);
    return allFiles;
  }

  async callGemini(prompt) {
    const url = `https://generativelanguage.googleapis.com/v1beta/models/gemini-1.5-flash:generateContent?key=${this.geminiApiKey}`;
    const response = await fetch(url, {
      method: 'POST',
      body: JSON.stringify({ contents: [{ parts: [{ text: prompt }] }] })
    });
    const data = await response.json();
    return data.candidates[0].content.parts[0].text;
  }
}

// Trigger analysis
async function run() {
    const [,, token, key, issueNum] = process.argv;
    const githubToken = token || process.env.GITHUB_TOKEN;
    const geminiApiKey = key || process.env.GEMINI_API_KEY;
    const issueNumber = issueNum || process.env.ISSUE_NUMBER;

    if (!githubToken || !geminiApiKey || !issueNumber) {
        console.error('❌ Missing environment variables: GITHUB_TOKEN, GEMINI_API_KEY, or ISSUE_NUMBER');
        process.exit(1);
    }

    try {
        const agent = new AIIssueAgent(githubToken, geminiApiKey);
        await agent.analyzeAndFix(parseInt(issueNumber));
    } catch (e) {
        console.error(`❌ Critical Error: ${e.message}`);
        process.exit(1);
    }
}

run();
